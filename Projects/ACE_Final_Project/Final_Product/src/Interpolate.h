// Function to perform binary search
int binarySearch(float x, float* arr, int size) {
  int low = 0;
  int high = size - 1;

  while (low <= high) {
    int mid = low + (high - low) / 2;

    if (arr[mid] == x) {
      return mid; // Found exact match
    } else if (arr[mid] < x) {
      low = mid + 1;
    } else {
      high = mid - 1;
    }
  }

  return -1; // Not found
}

// Function to lookup a value in the table
int lookupValue(float x, float* tableX, float* tableY, int size) {
  
  x = roundFloat(x, 2);
  
  if (x >= 17.00)
    x = 17.00;

  if ( x <= 7.5){
    x = 7.50;
  } 

  int index = binarySearch(x, tableX, size);
  if (index != -1) {
    return tableY[index];
  }
  // Handle the case where x is not found in the table
  return 0;  // You can choose a default value or handle it differently
}

